#tipo de dados -> int e float
'''
int -> numero inteiro
o tipo int representa qualquer numero
positivo ou negativo. int sem sinal e considerao positivo
exemplo
'''
print(11) #positivo - int
print(-11) #negativo - int
print(0) #int

'''
float -> numero com ponto flutuante
o tipo float representa qualquer numero positivo ou negativo
com ponto flutuante.
float sem sinal e considerado positivo.
'''
print(1.1,10.11) #float positivo
print(0.0,-1.5)  #float positivo e negativo

'''a função type mostra que tipo que o python inferiu(deduzio) ao valor '''

print(type('murilo'))
print(type(0))
print(type(0.1))
