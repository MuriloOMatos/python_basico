'''
Tipo de dado bool(boolean)
ao questionar algo em um programa,
so existem duas repostas possiveis:
sim(True)   Não(False)
existem varios operadores para 'questionar'.
entre eles o ==, que e um operador logico
que questiona se um valor e igual a outro.
'''
print(10==10) # Sim -> True
print(10==11) #Não  -> False
print(type(False)) #class bool
print(type(True))  #class bool
print(type(10==10)) #class bool
print(type(10==11)) #class bool